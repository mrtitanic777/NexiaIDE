/**
 * gen-templates.js — lift the project templates' file contents out of
 * src/main/projectManager.ts and emit them as C string literals.
 *
 * Generated rather than retyped, and that is the whole point. There are 918
 * lines of Xbox 360 C++ in those blobs. Hand-escaping them into C literals is
 * one slip away from a template that still compiles and does something subtly
 * wrong in somebody's new project — the kind of bug that surfaces on a console,
 * weeks later, in code the user assumes is the IDE's fault.
 *
 * The blobs are backtick literals with no ${...} in them (checked below, and
 * projectManager.ts says so in a comment at the top). So each one is evaluated
 * exactly the way TypeScript evaluates it, which is the only way to be sure the
 * escapes resolve the same.
 *
 *   node scripts/gen-templates.js
 *
 * Writes core/templates.c and core/templates.h. Do not edit those by hand — run
 * this instead. It is idempotent, so a diff after running it is a real change.
 */
const fs = require('fs');
const path = require('path');

const R = path.join(__dirname, '..');
const SRC = path.join(R, 'src', 'main', 'projectManager.ts');
const OUT_C = path.join(R, 'core', 'templates.c');
const OUT_H = path.join(R, 'core', 'templates.h');

const src = fs.readFileSync(SRC, 'utf-8');

/* ── find every `const NAME = \`...\`;` ──────────────────────────────────────── */
function extract(text) {
    const out = [];
    const re = /^const ([A-Z_0-9]+) = `/gm;
    let m;
    while ((m = re.exec(text))) {
        const name = m[1];
        const start = re.lastIndex;              // first char inside the backtick
        // Find the closing backtick that is not escaped.
        let i = start;
        for (;;) {
            const b = text.indexOf('`', i);
            if (b < 0) throw new Error(`${name}: unterminated template literal`);
            let slashes = 0;
            for (let j = b - 1; j >= 0 && text[j] === '\\'; j--) slashes++;
            if (slashes % 2 === 0) { i = b; break; }
            i = b + 1;
        }
        const raw = text.slice(start, i);
        if (raw.includes('${')) {
            // Would interpolate. Refuse rather than emit something plausible.
            throw new Error(`${name}: contains \${...}; this generator cannot evaluate it safely`);
        }
        // Evaluate it exactly as TypeScript would, so the escapes resolve identically.
        const value = new Function('return `' + raw + '`;')();
        out.push({ name, value });
    }
    return out;
}

/* ── C string literal, one source line per line of content ───────────────────── */
function cLiteral(s) {
    const lines = s.split('\n');
    const enc = (line) => {
        let o = '';
        for (let k = 0; k < line.length; k++) {
            const ch = line[k];
            const c = line.charCodeAt(k);
            if (ch === '\\') o += '\\\\';
            else if (ch === '"') o += '\\"';
            else if (ch === '\t') o += '\\t';
            else if (ch === '\r') o += '\\r';
            else if (ch === '?') o += '\\?';        // no accidental trigraphs
            else if (c >= 0x20 && c < 0x7f) o += ch;
            else {
                // Octal, not \x: \x is greedy in C and would swallow the next
                // hex digit of real text. UTF-8 bytes, because the TypeScript
                // writes these files as UTF-8 and the C must produce the same
                // bytes.
                for (const b of Buffer.from(ch, 'utf8'))
                    o += '\\' + b.toString(8).padStart(3, '0');
            }
        }
        return o;
    };
    // Every line but the last gets its newline back.
    return lines
        .map((l, idx) => '    "' + enc(l) + (idx < lines.length - 1 ? '\\n' : '') + '"')
        .join('\n');
}

const blobs = extract(src);
if (blobs.length === 0) throw new Error('no template literals found — did projectManager.ts change shape?');

const banner = (file) => `/*
 * ${file} — GENERATED by scripts/gen-templates.js. Do not edit.
 *
 * The project templates' file contents, lifted verbatim out of
 * src/main/projectManager.ts. Regenerate with:
 *
 *     node scripts/gen-templates.js
 *
 * ${blobs.length} templates, ${blobs.reduce((n, b) => n + b.value.split('\\n').length, 0)} lines of content.
 */
`;

let c = banner('templates.c') + '\n#include "templates.h"\n\n';
for (const b of blobs) {
    c += `/* ${b.name} — ${b.value.split('\n').length} lines */\n`;
    c += `const char TPL_${b.name}[] =\n${cLiteral(b.value)};\n\n`;
}

let h = banner('templates.h') + `
#ifndef NEXIA_TEMPLATES_H
#define NEXIA_TEMPLATES_H

`;
for (const b of blobs) h += `extern const char TPL_${b.name}[];\n`;
h += `
#endif
`;

fs.writeFileSync(OUT_C, c, 'utf-8');
fs.writeFileSync(OUT_H, h, 'utf-8');

console.log(`  ${blobs.length} templates -> core/templates.c, core/templates.h`);
for (const b of blobs)
    console.log(`    TPL_${b.name.padEnd(18)} ${String(b.value.split('\n').length).padStart(4)} lines, ${String(Buffer.byteLength(b.value, 'utf8')).padStart(6)} bytes`);
